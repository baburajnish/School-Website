function RegistrationUser(id, role, email, password, name, dob, mob, gender, qualifiction, address, country) {
    this.Id = "";
    this.Role = role;
    this.Email = email;
    this.Password = password;
    this.Name = name;
    this.Dob = dob;
    this.Mob = mob;
    this.Gender = gender;
    this.Qualifiction = qualifiction;
    this.Address = address;
    this.Country = country;

}